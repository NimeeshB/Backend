//In Express, if you use async/await in your route handlers and an error occurs, that error is a Promise rejection, and Express wonâ€™t catch it automatically unless you explicitly handle it.
//we would have to manually wrap every route in try catch. which is error prone
//hence we use this wrapper
const asyncHandler = (requestHandler) => {
    return (req, res, next) => {
        Promise.resolve(requestHandler(req, res, next)).catch((err) => next(err))
    }
}


export { asyncHandler }




